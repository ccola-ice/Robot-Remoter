/* FatFs多项功能测试 */
FRESULT miscellaneous(void)
{
  DIR dir;
  FATFS *pfs;
  DWORD fre_clust, fre_sect, tot_sect;
  
  printf("*************** 设备信息获取 ***************\r\n");
  /* 获取设备信息和空簇大小 */
  res = f_getfree("1:", &fre_clust, &pfs);

  /* 计算得到总的扇区个数和空扇区个数 */
  tot_sect = (pfs->n_fatent - 2) * pfs->csize;
  fre_sect = fre_clust * pfs->csize;

  /* 打印信息(4096 字节/扇区) */
  printf("设备总空间：%10lu KB。\r\n可用空间：  %10lu KB。\r\n", tot_sect *4, fre_sect *4);
  
  printf("******** 文件定位和格式化写入功能测试 ********\r\n");
  res = f_open(&fnew_flash, "1:FatFs读写测试文件.txt",FA_OPEN_ALWAYS|FA_WRITE|FA_READ );
	if ( res == FR_OK )
	{
    /*  文件定位 */
    res = f_lseek(&fnew_flash,f_size(&fnew_flash));
    if (res == FR_OK)
    {
      /* 格式化写入，参数格式类似printf函数 */
      f_printf(&fnew_flash,"原来文件d的新添加一行内容\r\n");
      f_printf(&fnew_flash,"设备总空间：%10lu KB。\r\n可用空间：  %10lu KB。", tot_sect *4, fre_sect *4);
      /*  文件定位到文件起始位置 */
      res = f_lseek(&fnew_flash,0);
      /* 读取文件所有内容到缓存区 */
      res = f_read(&fnew_flash,fatfs_read_buff,f_size(&fnew_flash),&fnum);
      if(res == FR_OK)
      {
        printf("文件内容：%s\r\n",fatfs_read_buff);
      }
    }
    f_close(&fnew_flash);    
    
    printf("********** 目录创建和重命名功能测试 **********\r\n");
    /* 尝试打开目录 */
    res=f_opendir(&dir,"1:TestDir");
    if(res!=FR_OK)
    {
      /* 打开目录失败，就创建目录 */
      res=f_mkdir("1:TestDir");
    }
    else
    {
      /* 如果目录已经存在，关闭它 */
      res=f_closedir(&dir);
      /* 删除文件 */
      f_unlink("1:TestDir/testdir.txt");
    }
    if(res==FR_OK)
    {
      /* 重命名并移动文件 */
      res=f_rename("1:FatFs读写测试文件.txt","1:TestDir/testdir.txt");      
    } 
	}
  else
  {
    printf("!! 打开文件失败：%d\n",res);
    printf("!! 或许需要再次运行“FatFs移植与读写测试”工程\n");
  }
  return res;
}

FILINFO fno;
/**
  * 文件信息获取
  */
FRESULT file_check(void)
{
  /* 获取文件信息 */
  res=f_stat("1:TestDir/testdir.txt",&fno);
  if(res==FR_OK)
  {
    printf("“testdir.txt”文件信息：\r\n");
    printf("文件大小: %ld(字节)\r\n", fno.fsize);
    printf("时间戳: %u/%02u/%02u, %02u:%02u\r\n",
           (fno.fdate >> 9) + 1980, fno.fdate >> 5 & 15, fno.fdate & 31,fno.ftime >> 11, fno.ftime >> 5 & 63);
    printf("属性: %c%c%c%c%c\n\r\n",
           (fno.fattrib & AM_DIR) ? 'D' : '-',      // 是一个目录
           (fno.fattrib & AM_RDO) ? 'R' : '-',      // 只读文件
           (fno.fattrib & AM_HID) ? 'H' : '-',      // 隐藏文件
           (fno.fattrib & AM_SYS) ? 'S' : '-',      // 系统文件
           (fno.fattrib & AM_ARC) ? 'A' : '-');     // 档案文件
  }
  return res;
}

/**
  * @brief  scan_files 递归扫描FatFs内的文件
  * @param  path:初始扫描路径
  * @retval result:文件系统的返回值
  */
FRESULT scan_files (char* path) 
{ 
  FRESULT res; 		//部分在递归过程被修改的变量，不用全局变量	
  FILINFO fno; 
  DIR dir; 
  int i;            
  char *fn;        // 文件名	
	
#if _USE_LFN 
  /* 长文件名支持 */
  /* 简体中文需要2个字节保存一个“字”*/
  static char lfn[_MAX_LFN*2 + 1]; 	
  fno.lfname = lfn; 
  fno.lfsize = sizeof(lfn); 
#endif 
  //打开目录
  res = f_opendir(&dir, path); 
  if (res == FR_OK) 
	{ 
    i = strlen(path); 
    for (;;) 
		{ 
      //读取目录下的内容，再读会自动读下一个文件
      res = f_readdir(&dir, &fno); 								
      //为空时表示所有项目读取完毕，跳出
      if (res != FR_OK || fno.fname[0] == 0) break; 	
#if _USE_LFN 
      fn = *fno.lfname ? fno.lfname : fno.fname; 
#else 
      fn = fno.fname; 
#endif 
      //点表示当前目录，跳过			
      if (*fn == '.') continue; 	
      //目录，递归读取      
      if (fno.fattrib & AM_DIR)         
			{ 			
        //合成完整目录名        
        sprintf(&path[i], "/%s", fn); 		
        //递归遍历         
        res = scan_files(path);	
        path[i] = 0;         
        //打开失败，跳出循环        
        if (res != FR_OK) 
					break; 
      } 
			else 
			{ 
				printf("%s/%s\r\n", path, fn);								//输出文件名	
        /* 可以在这里提取特定格式的文件路径 */        
      }//else
    } //for
  } 
  return res; 
}

